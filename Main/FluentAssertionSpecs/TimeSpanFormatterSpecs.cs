using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

using FluentAssertions.Formatting;

using Microsoft.VisualStudio.TestTools.UnitTesting;

namespace FluentAssertions.specs
{
    [TestClass]
    public class TimeSpanFormatterSpecs
    {
        [TestMethod]
        public void When_a_full_day_is_specified_it_should_only_include_the_day()
        {
            //-----------------------------------------------------------------------------------------------------------
            // Arrange
            //-----------------------------------------------------------------------------------------------------------            
            var formatter = new TimeSpanFormatter();

            //-----------------------------------------------------------------------------------------------------------
            // Act
            //-----------------------------------------------------------------------------------------------------------
            var result = formatter.ToString(TimeSpan.FromDays(2));

            //-----------------------------------------------------------------------------------------------------------
            // Assert
            //-----------------------------------------------------------------------------------------------------------
            result.Should().Be("2d");
        }        
        
        [TestMethod]
        public void When_only_hours_minutes_or_seconds_are_specified_it_should_use_a_time_format()
        {
            //-----------------------------------------------------------------------------------------------------------
            // Arrange
            //-----------------------------------------------------------------------------------------------------------            
            var formatter = new TimeSpanFormatter();

            //-----------------------------------------------------------------------------------------------------------
            // Act
            //-----------------------------------------------------------------------------------------------------------
            var result = formatter.ToString(new TimeSpan(0, 3, 20, 10, 58));

            //-----------------------------------------------------------------------------------------------------------
            // Assert
            //-----------------------------------------------------------------------------------------------------------
            result.Should().Be("3h20 10.58s");
        }        
        
        [TestMethod]
        public void When_only_days_and_seconds_are_specified_it_should_not_include_the_hours_and_minutes()
        {
            //-----------------------------------------------------------------------------------------------------------
            // Arrange
            //-----------------------------------------------------------------------------------------------------------            
            var formatter = new TimeSpanFormatter();

            //-----------------------------------------------------------------------------------------------------------
            // Act
            //-----------------------------------------------------------------------------------------------------------
            var result = formatter.ToString(new TimeSpan(2, 0, 0, 12, 0));

            //-----------------------------------------------------------------------------------------------------------
            // Assert
            //-----------------------------------------------------------------------------------------------------------
            result.Should().Be("2d 12.00s");
        }
    }
}
